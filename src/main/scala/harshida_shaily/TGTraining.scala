package harshida_shaily



class TGTraining {
  def add(a: Int, b: Int): Int = a + b

  def subtract(a: Int, b: Int): Int = {
    a - b
  }

  def multiplication(a: BigInt, b: BigInt) = a * b

  def division(a: Double, b: Double): Double = {
    a/b
  }

  def modulus(a: Int, b: Int): Int = {
    a % b
  }
}