class BasicMathOperations {

  def add(firstValue: Int, secondValue: Int) = {
    firstValue + secondValue
  }

  def subtract(firstValue: Int, secondValue: Int) = {
    firstValue - secondValue
  }

  def multiply(firstValue: Int, secondValue: Int) = {
    firstValue * secondValue
  }

  def divide(firstValue: Int, secondValue: Int) = {
    firstValue / secondValue
  }
}
